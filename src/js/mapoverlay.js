function openMapCardOverlay() {
  var x = document.querySelector(".map-card-overlay");
  if (x.style.display !== "none") {
    x.style.display = "none";
  } else {
    x.style.display = "block";
  }
}

function closeMapCardOverlay() {
  var x = document.querySelector(".map-card-overlay");
  if (x.style.display !== "none") {
    x.style.display = "none";
  } else {
    x.style.display = "block";
  }
}
