function openCreateOverlay() {
  var x = document.querySelector("#create-record-overlay");
  if (x.style.display !== "none") {
    x.style.display = "none";
  } else {
    x.style.display = "flex";
  }
}

function closeCreateOverlay() {
  var x = document.querySelector("#create-record-overlay");
  if (x.style.display !== "none") {
    x.style.display = "none";
  } else {
    x.style.display = "flex";
  }
}

